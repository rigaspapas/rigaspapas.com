window.onload = function(){
  var googleFonts = document.createElement('link');
  googleFonts.setAttribute('rel', 'stylesheet');
  googleFonts.setAttribute('type', 'text/css');
  googleFonts.setAttribute('href', 'http://fonts.googleapis.com/css?family=Roboto:400,300,500,700|Roboto+Condensed:400,300,700');
  document.getElementsByTagName('head')[0].appendChild( googleFonts );
}
